# Assignment 5: Implementation of Array Operations using NumPy

import numpy as np

# 1. Array Creation
arr1 = np.array([1, 2, 3, 4, 5])
arr2 = np.array([6, 7, 8, 9, 10])

print("Array 1:", arr1)
print("Array 2:", arr2)

# 2. Basic Array Operations
print("\n--- Basic Operations ---")
print("Addition:", arr1 + arr2)
print("Subtraction:", arr2 - arr1)
print("Multiplication:", arr1 * arr2)
print("Division:", arr2 / arr1)

# 3. Mathematical Operations
print("\n--- Mathematical Functions ---")
print("Square of arr1:", np.square(arr1))
print("Square Root of arr2:", np.sqrt(arr2))
print("Exponential of arr1:", np.exp(arr1))
print("Logarithm of arr2:", np.log(arr2))

